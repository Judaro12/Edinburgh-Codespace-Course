// loop for number from 1 to 15 // adding +1 each loop 
for (let i = 0; i <= 15; i++) {
    // checking if the remainder of the number by 2 is equal to zero
    if (i % 2 === 0) { 
        console.log(i + "is even"); // as its true it will print even 
    } else { 
        console.log(i + "is odd"); //as its false it will print odd 
    }
}


    

